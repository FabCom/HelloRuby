# #{} permet d'écrire du code dans une expression

# Affiche la phrase puis va à la ligne
puts "On va compter le nombre d'heures de travail à THP"

# Calcule et affiche le nombre d'heures restant pour la formation THP fullstack après l'expression
puts "Travail : #{10 * 5 * 11}"

# Calcul et affiche ce temps en minutes
puts "En minutes ça fait : #{10 * 5 * 11 * 60}"

# Affiche l'expression
puts "Et en secondes ?"

# Calcule et affiche uniquement le résultat
puts 10 * 5 * 11 * 60 * 60

# Affiche l'expression
puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"

# Demande si 3 + 2 est inférieur à 5-7 => affiche le résultat = false
puts 3 + 2 < 5 - 7

# Affiche l'expression  et le résultat de l'opération
puts "Ça fait combien 3 + 2 ? #{3 + 2}"

# idem
puts "Ça fait combien 5 - 7 ? #{5 - 7}"

# Affiche l'expression
puts "Ok, c'est faux alors !"

# Affiche l'expression
puts "C'est drôle ça, faisons-en plus :"

# Affiche l'expression et demande si 5 est strictement supérieur à -2
puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}"

# Affiche l'expression et demande si 5 est supérieur ou égal à -2
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}"

# Affiche l'expression et demande si 5 est inférieur ou égal à -2
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}"
